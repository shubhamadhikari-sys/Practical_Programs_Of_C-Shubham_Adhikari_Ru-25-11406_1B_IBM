 //WAP to compare two floating point numbers using relational operators.
 #include<stdio.h>
int main()
{
    float bal1=234.6,bal2=678.3;
    printf("Balance 1 = %f\n",bal1);
    printf("Balance 2 = %f\n",bal2);
    printf("Both balances are equal : %d \n",bal1==bal2);
    printf("First balance is greater than the second :%d\n",bal1>bal2);
    printf("Second balance is greater than first :%d \n",bal2>bal1);
    
}